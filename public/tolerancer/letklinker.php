            <h1>Fagligt forord </h1>
            
            <p>
                Denne håndbog er en del af Dansk Byggeris projekt om tværfaglige tolerancer, der 
                har til formål at forebygge konflikter som følge af forskellige opfattelser af kvalitetsniveauet 
                for et byggearbejde ved overgang fra en entreprise til den næste.
            </p>
            <p>
                For elementer af letklinkerbeton er beskrivelse af overfladekarakteristikken og måltolerancer væsentlige temaer.
                <br />
                Håndbogen er udarbejdet i samarbejde mellem brancheforeningen Dansk Beton Industriforenings Elementfraktion,
                BIH, og Dansk Byggeri og udgivet januar 2007.
            </p>

            <h3>Hvor går grænserne mellem leverandør, montør og maler?</h3>
            <p>
                Håndbogen er opdelt i tre hovedafsnit, der fastlægger grænserne mellem element
                leverandør, montagefirma og malerfirma:
                <ol>
                    <li>Tolerancer for producerede elementer fastlægger udfaldskravene til element-leverandøren</li>
                    <li>Tolerancer for monterede elementer fastlægger udfaldskravene til element-montøren</li>
                    <li>Tolerancer for overflader fastlægger indfaldskravene til malerfaget som kombinationen af 
                        udfaldskrav til leverandøren og montøren, dvs. til det enkelte element
                        og til de færdigmonterede elementer.
                    </li>
                </ol>
                I kapitlet Kontrolmetoder findes en beskrivelse af de metoder, som fagene er enige om, 
                egner sig bedst til at sætte tal på det aftalte niveau for kvaliteten.
            </p>

            <h3>Hvad er med?</h3>
            <p>
                Håndbogen omfatter vægge og dæk udført af elementer af letklinkerbeton og beskriver udseende af
                indvendige vægoverflader og dækundersider med henblik på valg af malerbehandling. 
                Desuden er særlige forhold vedrørende vægge og dæk, herunder fritstående kanter, vinduesfalse, 
                affasninger og samlinger mellem væg og dæk, beskrevet.
            </p>
            <p>
                Måltolerancer for enkelte støbte/producerede elementer og for monterede elementer er beskrevet 
                i hvert sit kapitel. Og endelig er der medtaget to selvstændige kapitler om udtørring af 
                elementer og nødvendige forarbejder for malerbehandling.
            </p>
            <p>
                Som bilag 1 er medtaget en kortfattet redegørelse for principperne i kvalitetssikring, 
                som udgør et væsentligt element i overholdelse af tolerancer. 
            </p>

