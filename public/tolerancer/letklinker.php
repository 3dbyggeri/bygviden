            <h3>Notat om kvalitetssikring</h3>
            <p>De gældende regler om kvalitetssikring af byggearbejder findes i bekendtgørelse nr. 169 af den 15. marts 2004. Udgangspunktet for denne bekendtgørelse er den kombinerede ansvars- og kvalitetsreform fra 1986, hvor der blev indført en femårig forældelse af ansvar kombineret med en pligt for byggeriets parter til at kvalitetssikre egne arbejder.
            </p>
            <p>
                Bekendtgørelsen er gældende for statsligt og statsstøttet byggeri, og hvor den er konkret aftalt mellem parterne. Bekendtgørelsen berører ikke direkte byggeriets ansvarsregler, men manglende kvalitetssikring kan i sig selv indebære en misligholdelse af kontraktforholdet.
            </p>
            <p>
                Formålet med kvalitetssikring er via en systematisk indsats at reducere omfanget af svigt ved primært at fokusere på kendte problemområder.
            </p>
            <p>Hovedprincipperne i bekendtgørelse er:
                <ul>
                    <li>Bygherren skal sikre, at byggeriet opnår den rette byggetekniske kvalitet, således at senere svigt modvirkes. Dette indebærer en pligt til at sætte kvaliteten for det konkrete byggeri</li>
                    <li>Rådgiverne og entreprenører skal hver for sig kvalitetssikre egne ydelser</li>
                    <li>Hovedvægten i kvalitetssikringen skal lægges på de enkelte områder, som erfaringsmæssigt har størst risiko for svigt, 
                    eller hvor konsekvensen af et svigt er særlig stor</li>
                    <li>Rådgiverne skal gennemføre en projektgranskning – herunder identificere særligt risikobehæftede bygningsdele og konstruktioner</li>
                    <li>Entreprenørerne skal - inden en eventuelt krævet projektgennemgang - udføre en procesgranskning</li>
                    <li>Der er metodefrihed med hensyn til udførelse af kvalitetssikring
                </ul>
            </p>
            <p>
                Det er en væsentlig forudsætning for at indfri bekendtgørelsens formål, at udbudsmaterialet indeholder en kontrolplan (udbudskontrolplanen) med de konkrete og specificerede krav til dokumentation af de kontraktsmæssige ydelser. Udover disse kontraktmæssige forpligtelser vil entreprenøren typisk selv have nogle forhold, som han ud fra egne erfaringer ønsker at kontrollere. Disse kontroller kan samles i en kvalitetsplan (eller kontrolplan) for det konkrete byggeri og udgør således den samlede kvalitetssikring. Bygherren har imidlertid ikke uden særskilt aftale herom krav på, at entreprenøren udfærdiger en samlet kvalitetsplan.
            </p>
           <p> 
                Kvalitetssikringen er altså en sagsbestemt dokumentation af kontraktmæssige ydelser i et specificeret omfang og et forhold imellem bygherren og entreprenøren.
            </p>
            <p>
                En del virksomheder har sat deres bestræbelser for at sikre kvaliteten i system, idet de har udarbejdet et kvalitetsstyringssystem for virksomheden. Heri er bl.a. virksomhedens kvalitetspolitik og retningslinier (procedurer) for opfyldelsen heraf nedfældet.
            </p>
            <p>
                Gennem sådanne interne procedurer/retningslinier kan virksomheden systematisere sin kontrol af, om grundlaget for udførelse af konditionsmæssigt arbejde er til stede samt af kontrol med udførelsen af eget arbejde. Med andre ord sætte fokus på indfaldskrav og udfaldskrav.
            </p>
            <p>
                For nærmere information om kvalitetssikring kan der henvises til www.danskbyggeri.dk under Erhvervsteknik.
            </p>
