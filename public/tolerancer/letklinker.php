                <h1>Ordforklaringer </h1>

                <h3>Afsmitning</h3>
                <p>
                    En overflade, der har væsentlig afsmitning, når man gnider på den, har 
                    ringe overfladefasthed og kan komplicere malerbehandlingen.
                </p>
                <p>
                    Det kan således være vanskeligt at opnå nødvendig vedhæftning til en afsmittende overflade.
                    I visse tilfælde er der en nær forbindelse mellem afsmitning og ringe sammenhængsstyrke, 
                    fx ved mangelfuldt hærdede pudslag.
                </p>
                <p>
                    Afsmitning kan være vanskelig at fjerne, bl.a. fordi der sjældent er en brat 
                    overgang mellem afsmittende flade og ikke-afsmittende flade. 
                    Byggestøv kan bevirke afsmit¬ning, men lader sig relativt let fjerne. 
                </p>

                <h3>Filtset flade</h3>
                <p>
                    Filtset flade er en overfladekarakter, som fremkommer, når en glat flade udfyldes eller 
                    repareres med finkornede, cementbundne materialer (dvs. med kornstørrelse under 0,3 mm), 
                    og der af¬sluttes med filtsning. Dybe og større reparationer eller udfyldninger kan 
                    indledningsvis udføres med grus og cement, men afsluttende behandling skal være finkornet. 
                    Udtrykket “ensartet filtset” flade betyder, at filtsningen skal være udført, 
                    så overfladekarakteren ikke er påfaldende uregelmæssig.
                </p>
                <p>
                    I praksis vil de omtalte udfyldninger eller reparationer ofte kunne udføres tilfredsstillende 
                    med spartling alene, dvs. uden afsluttende filtsning.
                </p>
                <h3>Glat flade</h3>
                <p>
                    Glat flade er betegnelsen for den overflade, der fremkommer ved støbning mod glat form uden væsentlige mekaniske skrammer.
                </p>
                <p>
                    På betonfladen kan forekomme små toppe, grater og spor.
                </p>

                <h3>Grater</h3>
                <p>
                    Grater er langstrakte forhøjninger på en i øvrigt jævn flade. Grater med små dimensioner 
                    kan have et mindre regelmæssigt forløb. Afhængig af den aktuelle flade kan grater også optræde 
                    som uregelmæssig afgrænsning fx af en elementsamling. 
                </p>
                <h3>Lokal planhedsafvigelse</h3>
                <p>
                    Lokale planhedsafvigelser er en samlet betegnelse for porer, revner, spor, prophuller, 
                    toppe, grater og betonudbedringer m.m. Den maksimalt acceptable lokale planhedsafvigelse skal angives. 
                <p>

