            <h2><span class="kapitel_num">3.3.2</span>Udtørringsmetoder</h2>
            <p>
                Der er mange faktorer, der afgør, hvor hurtigt vægelementer udtørrer: 
                Temperaturen og luftfugtigheden i rummet, typen af beton samt elementets tykkelse og fugtindhold. 
                Som tommelfingerregel bør temperaturen i bygningen ligge på 18-20°C for at sikre en jævn 
                fugtafgivelse fra elementerne. Højere temperatur giver risiko for overfladerevner (krakeleringer og netrevner), 
                og lavere temperatur giver lang udtørringstid.
            </p>
            <h3>Følgende udtørringsmetoder kan anvendes:</h3>
            <p>Langsom udtørring:  <span class="term">Opvarmning og ventilation</span></p>
            <p>
               Ved opvarmning og ventilation (åben radiator og vinduer på klem) vil den relative fugtighed ligge på op til 90 %, 
               hvis der ikke ventileres kraftigt. Udtørringen vil forløbe meget langsomt.
            </p>
            <p> Middelhurtig udtørring:  <span class="term">Kondenstørring</span></p>
            <p>
                Ved anvendelse af kondenstørrer kan der opnås en relativ luftfugtighed på 40 %. 
                Udtørringen forløber middelhurtigt. Metoden fungerer som et omvendt køleskab, 
                hvor luften suges gennem en køleflade, så vandindholdet kondenserer. 
                Kondenstørreren skal have afløb til kloak.
            </p>

            <div class="fakta_box">
                <h2>Almindelige forholdsregler ved brug af udtørringsanlæg</h2>
                <ul>
                    <li>Hold temperaturen på 18-20°c </li>
                    <li>Hold døre og vinder lukkede </li>
                    <li>Dimensionér udtørringsanlægget korrekt, og hold det konstant i drift.
                    </li>
                </ul>
            </div>
            
            <p>Hurtig udtørring:  <span class="term">Adsorptionstørring</span></p>
            <p>
                Med adsorptionstørring kan der opnås en relativ luftfugtighed på 10–20 %. 
                Udtørringen forløber hurtigt. Metoden fungerer ved, at luften suges gennem en adsorbent, 
                der optager vandindholdet i en række kamre, der regenereres efter tur. 
                Adsorptionstørreren skal have en lille ventilationsslange til det fri for at aflede 
                fugten fra regenereringsprocessen. (Se litteraturliste).
            </p>

