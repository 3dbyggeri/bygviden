            <h3><span class="img_nr">K1</span> Måling af fugtindhold </h3>
            <p>
                Ved måling af fugtindholdet i elementer er veje-tørre-metoden referencemetode. 
                For at finde stedet, hvor prøven til denne måling skal foretages, kan man finde det fugtigste 
                sted med en fugtindikator eller en RF-måling.
            </p>
            
            <h3>Veje- tørre-metoden</h3>
            <p>
                På et repræsentativt sted udbores en prøve (60–100 mm i diameter) ved tørboring. 
                Prøven emballeres umiddelbart herefter i en tæt plastpose og transporteres til laboratoriet, 
                hvor prøven vejes og tørres i et varmeskab ved 105 °C til konstant vægt. Prøven vejes igen, 
                og fugtindholdet bestemmes. Metoden giver en hurtig indikation af fugtindholdet, men er ikke præcis.
            </p>

            <h3>Fugtindikator (Gann måling)</h3>
            <p>
                Fugtindikatoren er et lille, elektrisk instrument, forsynet med en kugleformet målesonde (kuglehoved). 
                Kuglehovedet holdes mod elementet, og instrumentets display viser et relativt tal. 
                Med kendskab til betonens middeldensitet kan fugtindholdet bestemmes ved tabelopslag. Metoden er ikke helt præcis.
            </p>
            <h3>RF-måling</h3>
            <p>
                Princippet i RF-måling er, at den relative luftfugtighed måles i et boret hul i elementet. 
                Ved hjælp af sorptionskurver omsættes den aflæste relative luftfugtighed til fugtindholdet i elementet. 
                Metoden er følsom over for svingninger i temperatur. Under ideelle forhold er resultaterne pålidelige. 
            </p>

