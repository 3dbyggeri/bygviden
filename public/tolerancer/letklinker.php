                der ønskes på udspartlingen af samlingerne. Den bratte afvigelse ved samlinger måles 
                som niveauforskellen mellem de to sider af samlingen.
            </p>
            <h3>Porer</h3>
            <p>
                Porer er fordybninger i overfladen, der er forårsaget af letklinkerbetonens sammensætning. 
