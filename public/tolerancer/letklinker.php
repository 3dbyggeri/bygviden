            <h3>Planhed generelt</h3>
            <p>
                Planhed generelt angår afvigelsen fra plan flade inden for en formflades afgrænsning 
                eller inden for et elements afgrænsning. Generel planhed angives som jævnt stigende/jævnt 
                faldende afvigelse fra l m retskinne. Definitionen bygger på, at de hældnin¬ger, der forekommer 
                som følge af jævnt stigende eller jævnt faldende afvigelser fra retskinnen, altid er mindre end 1:100. 
                Jævnt stigende/jævnt faldende er forklaret under kontrolmetode F2 Planhed generelt og F4 Planhedsafvigelse ved samlinger (væg). 
            </p>
            <h3>Planhedsafvigelser ved elementsamlinger</h3>
            <p>
                Planhedsafvigelser ved elementsamlinger angår planhedsafvigelser som følge af fx 
                tykkelsesforskelle mellem elementer, elementmontage ol. Disse planhedsafvigelser kan 
                specificeres enten som bratte afvigelser eller som jævnt stigende/jævnt faldende afvigelser. 
