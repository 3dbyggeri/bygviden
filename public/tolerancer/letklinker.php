            <h3>Hvad er ikke med</h3>

            <p>
                Udvendige overflader ikke medtaget.
            </p>
            <p>
                Funktionsklasser, slutbehandling, den malermæssige overfladebehandling og forventet udfald er ikke beskrevet i 
                denne håndbog, hvorfor der henvises til Maler-håndbogen, 
                Malerfaglig Behandlings-Katalog, MBK, eller til malerleverandørernes anvisninger.
            </p>

            <div class="fakta_box">
                <h2>Valg af overfladespecifikationer</h2>
                <p>
                    Ved valg af overfladespecifikationer i det enkelte byggeprojekt må der tages hensyn 
                    til hvilke krav, der stilles til den færdig-behandlede overflade, 
                    herunder:
                    <ul>
                        <li>Overfladespecifikation for element</li>
                        <li>Funktionsklasse</li>
                        <li>Slutbehandling</li>
                        <li>Forventet udfald</li>
                    </ul>
                </p>
            </div>

            <p>
                Fastsættelse af tolerancer ændrer ikke på de grundlæggende ansvarsforhold mellem parterne.
            </p>
            <p>
                Ved fagentreprise er det således den enkelte fagentreprenørs ansvar over for bygherren, 
                at eventuelle tolerancekrav er overholdt. Endvidere er det bygherrens forpligtelse over 
                for den efterfølgende entreprenør, at arbejdsgrundlaget er egnet - 
                og er det på det fastlagte tidspunkt for igangsættelse af det efterfølgende arbejde.
            </p>
            <p>
                Modtagekontrol går som udgangspunkt ikke videre, end at den efterfølgende entreprenør 
                ikke må fortsætte på et arbejdsgrundlag, der åbenbart for ham ikke er egnet.
            </p>
            <p> 
                Finder den efterfølgende entreprenør, at arbejdsgrundlaget ikke er egnet, er det et forhold, 
                bygherrens tilsyn skal tage stilling til, og ikke et forhold, fagentreprenørerne 
                skal klare indbyrdes.
            </p>
                
