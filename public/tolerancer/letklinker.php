            <h3>Placering af fundamenter</h3>
            <p>
                Hvis fundamenter udføres i lempet toleranceklasse (LT) med kotetolerancer fra ±15 mm, er der risiko i 
                efterfølgende entrepriser for ekstraarbejder som fx:
                
                <ul>
                    <li>Opretning/afhugning for første skifte ved mur</li>
                    <li>Forøget opklodsning/afhugning ved de fleste typer facadeelementer.
                </ul>
                
                Ved en tolerance for afvigelser fra centerlinien på +10/-15 mm vil der være risiko for, at efterfølgende aktiviteter ikke kan optage denne tolerance. In situ-støbte fundamenter, betonfundamentsblokke inkl. letklinkerblokke samt præfabrikerede fundamenter anbefales derfor udført i normal eller skærpet toleranceklasse (NT eller ST) med følgende maksimale tolerancer:
                <ul>
                    <li>Kote o.s. +5/-15 mm.</li>
                    <li>Placering i forhold til centerlinie: +5/-10 mm.
                </ul>
                Det er en forudsætning, at alle mål afsættes og kontrolleres ud fra en fortløbende målkæde i et (x,y,z) koordinatsystem.
            </p>
            
            <h3>Indstøbninger i fundamenter</h3>
            <p>
                Der er ikke i danske standarder taget stilling til tolerancer for indstøbningsdele, såsom bolte, beslag etc. Der skal derfor i forbindelse med projekteringen tages stilling til nøjagtigheden for at sikre, at tolerancerne er nøjagtige nok. Hvor det er nødvendigt anvendes løsning som beskrevet i enten normal eller skærpet toleranceklasse:
                   <ul>
                    <li>NT (normal toleranceklasse): 	Placering i forhold til centerlinier: +/-8 mm.
                    </li>
                    <li>ST (skærpet toleranceklasse): 	Placering i forhold til centerlinier: +/-5 mm.</li>
                </ul>
                
                Ved afsætning af indstøbte dele med tolerance på ±5 mm eller med større nøjagtighed kan det anbefales, at der projekteres med udsparinger i fundament og efterfølgende placering ved indstøbning.  
                </p>
