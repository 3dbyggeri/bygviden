                <h3><span class="img_nr">J1</span> Undersøgelse af afsmitning ved aftørring </h3>

                <p>
                    En overflade, der smitter væsentligt af, når man gnider på den, har ringe overfladefasthed 
                    og kan komplicere malebehandling. For at bedømme graden af afsmitning benyttes metode 
                    SIS 18 41 97. Elementets overflade skal være tør, før der kan foretages en prøve.
                </p>
                <h3>Hjælpemidler</h3>
                <p>Rent, sort, blødt bomuldsstof.</p>

                <h3>Sådan gør du</h3>
                <ul>
                    <li>Fjern støv med støvkost eller støvsuger.</li>
                    <li>Fold stoffet, læg det omkring en finger, og før det én gang hen over den aktuelle flade med et 
                    jævnt let tryk i en bane på ca. 25 cm.
                    </li>
                </ul>

                <h3>Sådan bedømmes graden af afsmitning</h3>
                <ol>
                <li>Væsentlig afsmitning = tydelig hvid-/gråfarvning af stoffet.</li>
                <li>Uvæsentlig afsmitning = svag gråtoning af stoffet, som ikke øges afgørende, 
                hvis man gnider kluden mod den prøvede flade et par gange til.
                </li>
                <li>Ingen afsmitning.</li>
                </ol>
                
                <p>Hvis ikke andet er angivet i entreprisebeskrivelsen, er normen, at “Væsentlig afsmitning må ikke forekomme.” </p>
