            <h1>Forudsætninger </h1>
            <p>
               Når man arbejder med elementer af letklinkerbeton, er der en række forudsætninger, 
               der skal være på plads, for at et korrekt resultat kan opnås.
            </p>
            <p>
                Det er således afgørende vigtigt, at fundamentarbejdet med tilhørende indstøbninger er 
                udført med tolerancer, som svarer til indfaldskrav for elementmontagen. 
                Krav til fundamenter er derfor medtaget som bilag 2.
            </p>
            <p>
                I afsnit 3.3 Udtørring af elementer før overfladebehandling beskrives de væsentligste forhold vedrørende fugt, 
                udtørring og udtørringsmetoder, som er afgørende for, at fagene i fællesskab kan levere byggeri af høj kvalitet.
            </p>
            <p>
                I afsnit 3.4 Forarbejder ved malerbehandling beskrives nogle særlige forhold, der gør sig gældende i grænsefladen 
                mellem på den ene side leverandøren og montøren og på den anden side malerfaget. 
            </p>

            <h3>Om brugen af tolerancerne</h3>
            <ul>
                <li>
                    <span class="term">Enkeltmål</spam>. 
                    Tolerancer er angivet for enkeltmål.
                </li>
                <li>
                    <span class="term">Bearbejdning</term>. 
                    Overholdelse af montagetolerancerne kan i sjældne tilfælde medføre 
                    bearbejdning (tildannelse, pudsning) af elementerne på pladsen.
                </li>
                <li>
                    <span class="term">Særaftaler</term>. 
                    Den rådgivende ingeniør eller arkitekt må i den enkelte byggesag vurdere, 
                    om de angivne tolerancer er forenelige med de valgte løsninger i forhold til 
                    andre bygningsdele. Hvis der er særlige behov i det enkelte projekt, 
                    kan der aftales andre tolerancer. 
                </li>
            </ul>
