            <h3>Mulige konfliktområder</h3>
            <p>
                Ved anvendelse af tolerancerne i Produktstandarden DS/EN 1520 Præfabrikerede elementer af 
                letklinkerbeton, DS 420 Norm for letbetonkonstruktioner samt BIH’s vejledende tolerancer 
                kan der være konflikt mellem tolerancerne for råhuset og de efterfølgende aktiviteter. 
                Det kan fx dreje sig om:
                <ul>
                    <li>Montage af større vinduespartier mv.:</li>
                    <li>
                        Målfølsomme indvendige bygningsdele som trapper, elevatorer, branddøre, 
                        skabsnicher, køkkener, badeværelser mv.
                    </li>
                    <li>
                        Indvendigt gulvarbejde – herunder fastlæggelse af højder samt etablering af ensartede ud-/indgangsniveauer.
                    </li>
                    <li>
                        Indplacering af andre bærende elementer, dragere samt tagkonstruktion.
                    </li>
                </ul>
                
                Der er fx konflikt mellem Produktstandarden DS/EN 1520 Præfabrikerede elementer af letklinkerbeton og anneks 
                til DS 1003. Vinduer. Modulmål. Det kan derfor anbefales, at der sættes særligt fokus på tolerancer i byggeprocessen,
                der har betydning for efterfølgende aktiviteter.
            </p>
            <h3>Anbefalinger</h3>
            <ul>
                <li>
                    <span class="term">Overordnet analyse</term>. 
                    Det er nødvendigt at gennemanalysere bygningen og fastlægge de tværfaglige detaljer og 
                    tolerancer ud fra en overordnet sammenhæng. 
                    Det kan således være nødvendigt at tilpasse detaljer og tolerancer, så det ønskede resultat kan opnås af de forskellige fag.
                </li>
                <li>
                    <span class="term">Udsparinger til vinduer og døre</span>. 
                    Det anbefales at målsætte udsparinger til vinduer og døre med +10 mm i forhold til 
                    det teoretiske hulmål for at imødegå evt. toleranceafvigelser ved elementproduktionen, pilhøjder og montagen.
                </li>
                <li>
                    <span class="term">Flugtende vinduer og døre</span>. 
                    Udsparinger til vinduer og døre kan, hvor vinduer skal flugte vandret og/eller lodret, som alternativ 
                    til skærpet montagenøjagtighed udføres med et overmål i bredde og højde på fx 30 mm. 
                    Efterfølgende udføres pålimning af false, der indmåles og monteres efter elementmontagen.
                </li>
            </ul>
            <p>
                De angivne måltolerancer for de enkelte vægge og dæk beskriver udfaldskravene til leverandøren – 
                og dermed indfaldskravene for montøren. 
            </p>
            
