        <h2><span class="kapitel_num">3.3.1</span> Fugt og udtørring</h2> 
        <p>De fleste byggematerialer svinder under udtørring og risikerer derfor at slå revner. 
            Dette gælder også elementer af letklinkerbeton. For at forebygge skader i den færdige behandling 
            af overfladerne skal der foretages en styret udtørring, før maler- og flisearbejdet påbegyndes.
        </p>
