            <h1>På vej mod højere kvalitet og produktivitet </h1>
            <p>
                Byggeriet er i stigende grad blevet komplekst. Den opsplittede byggeproces og pres 
                på tid og økonomi giver en række udfordringer for projektering og udførelse og for 
                den planlægning og koordinering, der er helt afgørende for alle parter. 
                Samtidig er der behov for en forstærket indsats for at forbedre byggeriets kvalitet og produktivitet.
                Men der er ingen nemme løsninger på disse udfordringer. 
                Resultaterne skal nås ved at fokusere på en række områder, som hver især kan bidrage til den ønskede udvikling.
                Entreprenører og håndværksvirksomheder har ofte peget på, at nogle af de væsentlige årsager til kvalitetsproblemer skal findes i 
                grænsefladerne mellem bygningsdelene og de mange forskellige fag, der er involveret i en byggesag. 
                Der er således talrige eksempler fra praksis og analyser på, at der i projektering og planlægning ikke 
                er gjort tilstrækkeligt for at sikre, at udfaldskrav for ét fag stemmer overens med indfaldskrav 
                for de efterfølgende fag.
                Dansk Byggeri ønsker at medvirke aktivt til at forbedre byggeriets kvalitet og produktivitet og finder, 
                at en øget fokusering på de problemer, der opstår i grænsefladerne mellem konstruktionsdele og fag, 
                vil kunne bidrage til dette formål. Derfor har vi taget initiativ til at fastlægge tolerancer og måleregler 
                på nogle væsentlige faglige områder inden for byggeriet. Initiativets resultat foreligger nu i form af en række håndbøger, 
                som hver på sit faglige område afklarer fagets tolerancer med særlig vægt på de tolerancer, 
                som ligger i grænsefladerne til andre fag. 
            </p>

